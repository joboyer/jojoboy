#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t		i;
	size_t		len;

	i = 0;
	if (s != NULL && f != NULL)
	{
		len = ft_strlen(s);
		while (i < len)
		{
			f(s);
			s++;
			i++;
		}
	}
}
